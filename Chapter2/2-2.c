// 문제 2-2
// 정숫값을 읽어 들여 5로 나눈 몫과 나머지를 출력하는 프로그램을 작성하자.

#include <stdio.h>

int main(void) {
	int n;

	printf("정수 n을 입력하세요 : ");
	scanf("%d", &n);

	printf("n/5는 %d입니다.\n", n/5);
	printf("n%%5는 %d입니다.\n", n%5);

	return 0;
}

