// 문제 4-27_4
// 읽어 들인 정수를 짧은 변의 길이로 하는 직각이등변삼각형을 출력하는 프로그램을 작성하자.
// 왼쪽 아래가 직각인 삼각형, 왼쪽 위가 직각인 삼각형, 오른쪽 아래가 직각인 삼각형, 오른쪽 위가 직각인 삼각형을 각각 만든다.

#include <stdio.h>

int main(void) {
	int i, j, len;

	puts("오른쪽 위가 직각인 직각이등변삼각형을 만듭니다.");
	printf("짧은 변 : ");
	scanf("%d", &len);

	for (i=len; i>=1; i--) {
		for (j=1; j<=len-i; j++)
			putchar(' ');
		for (j=1; j<=i; j++)
			putchar('*');
		putchar('\n');
	}

	return 0;
}

// 직각삼각형 그리기
// 프로그램 모두 삼각형의 짧은 변 길이를 변수 len으로 읽어 들여 len 행만큼 출력한다.
