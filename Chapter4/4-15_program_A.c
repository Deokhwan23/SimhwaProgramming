// 문제 4-15(프로그램 A)
// 1234567890을 반복 출력하는 프로그램을 작성하자. 읽어 들인 정숫값의 개수만큼 숫자를 출력한다.
// (예를 들어 25를 입력했다면 1234567890123456789012345라고 출력한다).

#include <stdio.h>

int main(void) {
	int i, no;

	printf("양의 정수를 입력해주세요 : ");
	scanf("%d", &no);

	for (i=1; i<=no; i++)
		printf("%d", i%10);
	putchar('\n');

	return 0;
}

// 프로그램 A는 i값을 1부터 no까지 인크리먼트하면서 그 값을 10으로 나눈 나머지를 표시한다.
// 프로그램은 기대한 대로 동작하지만, 나눗셈을 no번 수행하므로 효율 면에서 좋지 않다.
